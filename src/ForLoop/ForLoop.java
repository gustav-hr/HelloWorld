package ForLoop;

public class ForLoop {
    public static void main(String[] args) {

        // I et for-loop er der 3 "stykker kode".

        // Den første er der hvor man starter. Her er det = 0, så vi starter på 0.
        // Den anden er der hvor loopet skal slutte.


        for (int i = 0; i <= 10; i += 2) {

            System.out.println(i);


            }
        }
    }
